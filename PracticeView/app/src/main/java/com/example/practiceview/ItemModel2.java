package com.example.practiceview;

public class ItemModel2 {
    private int image;
    private String name;
    private int age;

    public ItemModel2(int image, String name, int age) {
        this.image = image;
        this.name = name;
        this.age = age;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
